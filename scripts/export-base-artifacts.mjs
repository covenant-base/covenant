#!/usr/bin/env node

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';

const root = resolve(new URL('..', import.meta.url).pathname);
const outDir = join(root, 'contracts', 'out');
const abiDir = join(root, 'packages', 'sdk', 'src', 'generated', 'abis');
const generatedIndexPath = join(root, 'packages', 'sdk', 'src', 'generated', 'contracts.ts');

const manifestFiles = [
  'base.json',
  'baseSepolia.json',
  'localBase.json',
];

const contractMap = {
  agentRegistry: 'CovenantAgentRegistry.sol/CovenantAgentRegistry.json',
  capabilityRegistry: 'CovenantCapabilityRegistry.sol/CovenantCapabilityRegistry.json',
  treasury: 'CovenantTreasury.sol/CovenantTreasury.json',
  taskMarket: 'CovenantTaskMarket.sol/CovenantTaskMarket.json',
  proofVerifier: 'CovenantProofVerifier.sol/CovenantProofVerifier.json',
  disputeArbitration: 'CovenantDisputeArbitration.sol/CovenantDisputeArbitration.json',
  governance: 'CovenantGovernance.sol/CovenantGovernance.json',
  feeCollector: 'CovenantFeeCollector.sol/CovenantFeeCollector.json',
  staking: 'CovenantStaking.sol/CovenantStaking.json',
  templateRegistry: 'CovenantTemplateRegistry.sol/CovenantTemplateRegistry.json',
  token: 'CovenantToken.sol/CovenantToken.json',
};

mkdirSync(abiDir, { recursive: true });

const exportLines = [
  '// Auto-generated by scripts/export-base-artifacts.mjs',
];

for (const [key, rel] of Object.entries(contractMap)) {
  const sourcePath = join(outDir, rel);
  const targetPath = join(abiDir, `${key}.json`);
  const raw = JSON.parse(readFileSync(sourcePath, 'utf8'));
  writeFileSync(targetPath, `${JSON.stringify(raw.abi, null, 2)}\n`);
  exportLines.push(`import ${key}Abi from './abis/${key}.json';`);
}

exportLines.push('');
exportLines.push('export const covenantContractAbis = {');
for (const key of Object.keys(contractMap)) {
  exportLines.push(`  ${key}: ${key}Abi,`);
}
exportLines.push('} as const;');
exportLines.push('');
exportLines.push('export type CovenantContractAbiMap = typeof covenantContractAbis;');

writeFileSync(generatedIndexPath, `${exportLines.join('\n')}\n`);

for (const file of manifestFiles) {
  const manifestPath = join(root, 'packages', 'config', 'deployments', file);
  const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
  writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
}
